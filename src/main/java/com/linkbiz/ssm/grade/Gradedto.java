package com.linkbiz.ssm.grade;

public class Gradedto {
	
	private String school;
	private String className;
	private String numericName; 
	private String teacher; 
	private double monthlyTuitionFee;
	private double admissionFee; 
	private double examFee;
	private double certificateFee;
	private String note;
	
	public String getSchool() {
		return school;
	}
	public void setSchool(String school) {
		this.school = school;
	}
	public String getClassName() {
		return className;
	}
	public void setClassName(String className) {
		this.className = className;
	}
	public String getNumericName() {
		return numericName;
	}
	public void setNumericName(String numericName) {
		this.numericName = numericName;
	}
	public String getTeacher() {
		return teacher;
	}
	public void setTeacher(String teacher) {
		this.teacher = teacher;
	}
	public double getMonthlyTuitionFee() {
		return monthlyTuitionFee;
	}
	public void setMonthlyTuitionFee(double monthlyTuitionFee) {
		this.monthlyTuitionFee = monthlyTuitionFee;
	}
	public double getAdmissionFee() {
		return admissionFee;
	}
	public void setAdmissionFee(double admissionFee) {
		this.admissionFee = admissionFee;
	}
	public double getExamFee() {
		return examFee;
	}
	public void setExamFee(double examFee) {
		this.examFee = examFee;
	}
	public double getCertificateFee() {
		return certificateFee;
	}
	public void setCertificateFee(double certificateFee) {
		this.certificateFee = certificateFee;
	}
	public String getNote() {
		return note;
	}
	public void setNote(String note) {
		this.note = note;
	}
	

}
